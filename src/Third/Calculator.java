package Third;

public class Calculator {
    public double sum(double numb, double numb2){
        return numb + numb2;
    }
    public double dif(double numb, double numb2) {
        return numb - numb2;
    }
    public double div(double numb, double numb2) {
        return numb / numb2;
    }
    public double daug(double numb, double numb2) {
        return numb * numb2;
    }
    public double kvad(double numb) {
        return Math.pow(numb, 2);

    }

}
